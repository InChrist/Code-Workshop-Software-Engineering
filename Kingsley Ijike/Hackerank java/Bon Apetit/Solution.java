    // Complete the bonAppetit function below.
    static void bonAppetit(List<Integer> bill, int k, int b) {
        int aBill = bill.get(k);//Anna's bill  
        
        bill.remove(k);//remove it from the list
        
        int billSum = bill.stream().mapToInt(i -> i).sum();//add up to the rest
        int halfSum = billSum/2;//get the halved bill
        
        //check if Anna needs to be reinbursed.
        if(halfSum==b) System.out.println("Bon Appetit");
        else System.out.println(b-halfSum);


    }

    public static void main(String[] args) throws IOException {
        BufferedReader bufferedReader = new BufferedReader(new InputStreamReader(System.in));

        String[] nk = bufferedReader.readLine().replaceAll("\\s+$", "").split(" ");

        int n = Integer.parseInt(nk[0]);

        int k = Integer.parseInt(nk[1]);

        List<Integer> bill = Stream.of(bufferedReader.readLine().replaceAll("\\s+$", "").split(" "))
            .map(Integer::parseInt)
            .collect(toList());

        int b = Integer.parseInt(bufferedReader.readLine().trim());

        bonAppetit(bill, k, b);

        bufferedReader.close();
    }
